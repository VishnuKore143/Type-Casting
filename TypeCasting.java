package Practices;

public class TypeCasting {
	public static void main(String[]args) {
		
	// Automatic Type Casting
	int x = 25;
	int y = x;
	System.out.println(y);
	
	
	// Manual Type Casting
	double myDouble = 12.58;
	int myInt =(int)myDouble;
	System.out.println(myInt);
	
	

	}
	
}
